"""
Simulation Results Artifact Schema
==================================

Pydantic model for simulation results learned artifact.
Contains comprehensive simulation data with extensive metadata.
"""

from pydantic import BaseModel, Field, ValidationError
from typing import Dict, List, Optional, Any
from pathlib import Path
import json


class TopicProbabilityWithEmbedding(BaseModel):
    """Topic probability with optional embedding."""
    probability: float = Field(..., ge=0.0, le=1.0, description="Topic probability score")
    embedding: Optional[List[float]] = Field(None, description="Embedding vector for this topic")


class SimulationResultItem(BaseModel):
    """Individual simulation result item with comprehensive metadata."""
    user_id: str = Field(..., description="Unique user identifier")
    num_reviews_of_user: int = Field(..., ge=0, description="Total number of reviews by this user")
    tribe_name: str = Field(..., description="Name of the tribe (micro cluster persona)")
    tribe_id: str = Field(..., description="Tribe identifier (micro cluster ID)")
    num_users_in_tribe: int = Field(..., ge=0, description="Total number of users in this tribe")
    cluster_name: str = Field(..., description="Cluster/Segment name (macro cluster)")
    cluster_id: str = Field(..., description="Cluster/Segment identifier")
    num_tribes_in_cluster: int = Field(..., ge=0, description="Total number of tribes in this cluster")
    stimulus_product_id: str = Field(..., description="Product ID (ASIN or product identifier)")
    stimulus_product_description: str = Field(..., description="Product description")
    real_review: str = Field(..., description="Actual review text written by the user")
    real_review_embedding: List[float] = Field(..., description="Embedding vector for the real review")
    rating: float = Field(..., ge=1.0, le=5.0, description="Actual rating given by the user")
    real_topic_probabilities: Dict[str, TopicProbabilityWithEmbedding] = Field(
        ...,
        description="Topic probabilities with embeddings for real review"
    )
    synthetic_review: str = Field(..., description="Synthetic review text generated by the model")
    synthetic_review_embedding: List[float] = Field(..., description="Embedding vector for the synthetic review")
    synthetic_topic_probabilities: Dict[str, TopicProbabilityWithEmbedding] = Field(
        ...,
        description="Topic probabilities with embeddings for synthetic review"
    )
    sentiment_predicted: str = Field(..., pattern="^(Positive|Negative|Neutral)$", description="Predicted sentiment")
    rating_predicted: float = Field(..., ge=1.0, le=5.0, description="Predicted rating")

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> 'SimulationResultItem':
        """Load and validate SimulationResultItem from a dictionary."""
        try:
            # Convert topic probabilities format if needed
            if "real_topic_probabilities" in data:
                real_topic_probs = data["real_topic_probabilities"]
                if isinstance(real_topic_probs, dict):
                    converted_real = {}
                    for topic, prob_data in real_topic_probs.items():
                        if isinstance(prob_data, dict):
                            converted_real[topic] = TopicProbabilityWithEmbedding(**prob_data)
                        else:
                            # Simple probability value
                            converted_real[topic] = TopicProbabilityWithEmbedding(probability=float(prob_data))
                    data["real_topic_probabilities"] = converted_real
            
            if "synthetic_topic_probabilities" in data:
                synth_topic_probs = data["synthetic_topic_probabilities"]
                if isinstance(synth_topic_probs, dict):
                    converted_synth = {}
                    for topic, prob_data in synth_topic_probs.items():
                        if isinstance(prob_data, dict):
                            converted_synth[topic] = TopicProbabilityWithEmbedding(**prob_data)
                        else:
                            # Simple probability value
                            converted_synth[topic] = TopicProbabilityWithEmbedding(probability=float(prob_data))
                    data["synthetic_topic_probabilities"] = converted_synth
            
            return cls(**data)
        except ValidationError as e:
            raise ValueError(f"Schema validation failed for simulation result item: {e}")

    def to_dict(self) -> Dict[str, Any]:
        """Convert the model instance to a dictionary."""
        return self.model_dump(exclude_none=True, by_alias=True)


class SimulationResultsArtifact(BaseModel):
    """
    Schema for Simulation Results learned artifact.
    Contains comprehensive simulation data with extensive metadata.
    Structure: {review_key: SimulationResultItem}
    """
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> Dict[str, SimulationResultItem]:
        """
        Load and validate simulation results from a dictionary.
        
        Args:
            data: Dictionary mapping review_key to simulation result data
            
        Returns:
            Dictionary mapping review_key to validated SimulationResultItem instances
        """
        validated = {}
        for review_key, item_data in data.items():
            try:
                validated[review_key] = SimulationResultItem.from_dict(item_data)
            except Exception as e:
                raise ValueError(f"Validation error for review {review_key}: {e}")
        
        return validated

    @classmethod
    def from_file(cls, file_path: Path) -> Dict[str, SimulationResultItem]:
        """
        Load and validate simulation results from a JSON file.
        
        Args:
            file_path: Path to the JSON file containing simulation results
            
        Returns:
            Dictionary mapping review_key to validated SimulationResultItem instances
        """
        with open(file_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        if not isinstance(data, dict):
            raise ValueError(f"Expected dictionary in {file_path}, got {type(data)}")
        
        return cls.from_dict(data)

    @staticmethod
    def to_dict(simulation_results_dict: Dict[str, SimulationResultItem]) -> Dict[str, Dict[str, Any]]:
        """
        Convert a dictionary of SimulationResultItem instances to a dictionary.
        
        Args:
            simulation_results_dict: Dictionary mapping review_key to SimulationResultItem instances
            
        Returns:
            Dictionary mapping review_key to simulation result data dictionaries
        """
        return {key: item.to_dict() for key, item in simulation_results_dict.items()}

